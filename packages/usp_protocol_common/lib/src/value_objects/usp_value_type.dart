/// An enumeration of USP (User Services Platform) data types.
enum UspValueType {
  /// A string value.
  string,

  /// An integer value.
  int,

  /// An unsigned integer value.
  unsignedInt,

  /// A long integer value.
  long,

  /// An unsigned long integer value.
  unsignedLong,

  /// A boolean value.
  boolean,

  /// A date and time value.
  dateTime,

  /// A base64-encoded binary value.
  base64,

  /// A hexadecimal binary value.
  hexBinary,
}
