/// Represents a category of devices, used for displaying appropriate icons in the UI.
enum IconDeviceCategory {
  /// An unknown or uncategorized device type.
  unknown,

  /// A device connected via Ethernet, like a router or a switch.
  ethernet,

  /// A smartphone or a VoIP phone.
  phone,

  /// A desktop, laptop, or tablet computer.
  computer,

  /// A smart TV or a streaming media player.
  tv,

  /// A smart speaker or a sound system.
  speaker,

  /// A security camera or a webcam.
  camera,

  /// A smart doorbell.
  doorbell,

  /// A smart lock.
  lock,

  /// A smartwatch.
  watch,

  /// A smart plug or an outlet.
  plug,

  /// A smart light bulb.
  bulb,

  /// A robotic vacuum cleaner.
  vacuum,

  /// A gaming console like PlayStation, Xbox, or Nintendo Switch.
  gameConsole,
}
